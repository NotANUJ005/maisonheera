export const splitCatalogByType = (items) => {
  const normalizedItems = items.map((item, index) => normalizeProduct(item, index));

  return {
    all: normalizedItems,
    products: normalizedItems.filter((item) => item.type === 'product'),
    prestige: normalizedItems.filter((item) => item.type === 'prestige'),
  };
};
