const slugify = (value) =>
  String(value || '')
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/(^-|-$)/g, '');

const inferCollection = (product) => {
  if (product.collection) return product.collection;
  if (product.type === 'prestige') return 'Prestige Archives';
  return product.category || 'Archive';
};

export const normalizeProduct = (product, index = 0) => {
  const type = product.type === 'prestige' ? 'prestige' : 'product';
  const id = product.id || product._id || `${type}-${slugify(product.name) || index}`;
  const category = product.category || (type === 'prestige' ? 'High Jewelry' : 'Jewelry');
  const material = product.material || 'Gold';
  const fallbackImage = BASE_CATALOG[index % BASE_CATALOG.length]?.image || BASE_CATALOG[0].image;

  return {
    id,
    _id: product._id || id,
    type,
    name: product.name || 'Untitled Piece',
    price: Number(product.price) || 0,
    category,
    material,
    collection: inferCollection({ ...product, type, category }),
    tier: product.tier || (type === 'prestige' ? 'Prestige' : 'Signature'),
    rating: Number.isFinite(Number(product.rating)) && Number(product.rating) > 0 ? Number(product.rating) : null,
    ratingCount: Math.max(Number(product.ratingCount) || 0, 0),
    stockQuantity: Number(product.stockQuantity) || 25,
    featured: Boolean(product.featured),
    description: product.description || 'A meticulously crafted Maison Heera creation.',
    image: product.image || fallbackImage,
  };
};

const normalizedCatalog = BASE_CATALOG.map((item, index) => normalizeProduct(item, index));

export const LUXURY_PRODUCTS = normalizedCatalog;
export const PRODUCTS = normalizedCatalog.filter((item) => item.type === 'product');
export const PRESTIGE_ITEMS = normalizedCatalog.filter((item) => item.type === 'prestige');

